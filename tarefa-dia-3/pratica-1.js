// Considere que o dado de entrada para executar o código é o número n. Ou seja, uma variável nque vai querer 
// o valor do número final que exibir;
// Neste problema, você deve usar o comando while;
// Exiba todos os números de 1 a n. Por exemplo, se o valor de n for 9, você deverá exibir o seguinte resultado
// na tela com console.log();

let n = 9
let contador = 1

while (contador <= n) {
    console.log(contador)
    contador ++
}